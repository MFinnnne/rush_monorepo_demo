// add function
export function add(a: number, b: number) {
	return a + b
}

// minus function
export function minus(a: number, b: number) {
	return a - b
}

// multiply function
export function multiply(a: number, b: number) {
	return a * b
}

// divide function
export function divide(a: number, b: number) {
	return a / b
}
